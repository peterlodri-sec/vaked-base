/// `_render_policy`: a fiber's `policy { … }` summary for RUNTIME.md.
fn renderPolicy(alloc: Alloc, fiber: *const GraphNode) ![]const u8 {
    const fields = try fiberPolicyFields(alloc, fiber);
    if (fields.len == 0) return "\u{2014}";
    var parts: std.ArrayList([]const u8) = .empty;
    for (fields) |kv| {
        switch (kv.value) {
            .bool => |b| try parts.append(alloc, try std.fmt.allocPrint(alloc, "{s} = {s}", .{ kv.name, if (b) "true" else "false" })),
            .array => |items| {
                var ab: Buf = .empty;
                for (items, 0..) |x, i| {
                    if (i != 0) try ab.appendSlice(alloc, ", ");
                    try ab.appendSlice(alloc, try std.fmt.allocPrint(alloc, "\"{s}\"", .{scalarStr(x)}));
                }
                try parts.append(alloc, try std.fmt.allocPrint(alloc, "{s} = [{s}]", .{ kv.name, ab.items }));
            },
            else => try parts.append(alloc, try std.fmt.allocPrint(alloc, "{s} = \"{s}\"", .{ kv.name, scalarStr(kv.value) })),
        }
    }
    // "`" + "`, `".join(parts) + "`"
    var buf: Buf = .empty;
    try buf.append(alloc, '`');
    for (parts.items, 0..) |p, i| {
        if (i != 0) try buf.appendSlice(alloc, "`, `");
        try buf.appendSlice(alloc, p);
    }
    try buf.append(alloc, '`');
    return buf.toOwnedSlice(alloc);
}

const PolicyField = struct { name: []const u8, value: Value };

/// `_fiber_policy_fields`: project a fiber's policy fields in source order as
/// `[(name, value)]`. Each value is its scalar projection.
fn fiberPolicyFields(alloc: Alloc, fiber: *const GraphNode) ![]const PolicyField {
    var out: std.ArrayList(PolicyField) = .empty;
    const pol = nodeProp(fiber, "policy") orelse return out.toOwnedSlice(alloc);
    const rec = field(pol, "record") orelse return out.toOwnedSlice(alloc);
    switch (rec) {
        .array => |items| {
            for (items) |e| {
                if (field(e, "assign")) |assign_v| {
                    const name = switch (assign_v) {
                        .string => |s| s,
                        else => continue,
                    };
                    const val = scalarProp(alloc, field(e, "value")) catch continue;
                    try out.append(alloc, .{ .name = name, .value = val });
                }
            }
        },
        else => {},
    }
    return out.toOwnedSlice(alloc);
}

/// A scalar `Value` rendered as a display string (for policy table cells).
fn scalarStr(v: Value) []const u8 {
    switch (v) {
        .string => |s| return s,
        .bool => |b| return if (b) "true" else "false",
        else => return "",
    }
}

/// `_render_ref_list`: comma-joined code spans of a list/single ref prop.
fn renderRefList(alloc: Alloc, prop: ?Value) ![]const u8 {
    var parts: std.ArrayList([]const u8) = .empty;
    const p = prop orelse return joinComma(alloc, parts.items);
    switch (p) {
        .array => |items| {
            for (items) |x| {
                if (refOf(x)) |r| try parts.append(alloc, r);
            }
        },
        else => {
            if (refOf(p)) |r| try parts.append(alloc, r);
        },
    }
    var coded: std.ArrayList([]const u8) = .empty;
    for (parts.items) |r| try coded.append(alloc, try mdCode(alloc, r));
    return joinComma(alloc, coded.items);
}

/// `_implied_membrane`: the implied membrane string for a stream source channel.
fn impliedMembrane(source: ?[]const u8) []const u8 {
    const s = source orelse return "\u{2014}";
    if (std.mem.startsWith(u8, s, "agentGuardd.")) return "`ebpf` (agent-guardd)";
    if (std.mem.startsWith(u8, s, "agentpipe.")) return "`media` capture";
    return "\u{2014}";
}

