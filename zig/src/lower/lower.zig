/// nixpkgs is emitted PINNED (0012 §4.1). The 40-hex value is a disclosed
/// placeholder standing in for the toolchain's pinned baseline rev.
const NIXPKGS_BASELINE_REV = "bbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbb";

