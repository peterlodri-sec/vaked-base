/// `_children_of`: direct `contains` children of a node, in source order. Uses
/// `edgesFrom` (O(1) memoized adjacency lookup) — output order is byte-identical
/// to the old full scan.
fn childrenOf(alloc: Alloc, graph: *Graph, parent_id: []const u8) ![]*GraphNode {
    var out: std.ArrayList(*GraphNode) = .empty;
    const edges = try graph.edgesFrom(parent_id);
    for (edges) |e| {
        if (std.mem.eql(u8, e.label, "contains")) {
            if (graph.getNode(e.to)) |child| try out.append(alloc, child);
        }
    }
    return out.toOwnedSlice(alloc);
}

fn byKind(alloc: Alloc, nodes: []*GraphNode, kind: []const u8) ![]*GraphNode {
    var out: std.ArrayList(*GraphNode) = .empty;
    for (nodes) |n| {
        if (std.mem.eql(u8, n.kind, kind)) try out.append(alloc, n);
    }
    return out.toOwnedSlice(alloc);
}

const RuntimeView = struct {
    runtime: *GraphNode,
    indexes: []*GraphNode,
    streams: []*GraphNode,
    fibers: []*GraphNode,
    surfaces: []*GraphNode,
    parallels: []*GraphNode,
};

fn runtimeView(alloc: Alloc, graph: *Graph) !?RuntimeView {
    // runtimes = [n for n in graph.nodes_sorted() if n.kind == "runtime"]; take [0].
    const sorted = try graph.nodesSorted();
    var runtime_id: ?[]const u8 = null;
    for (sorted) |n| {
        if (std.mem.eql(u8, n.kind, "runtime")) {
            runtime_id = n.id;
            break;
        }
    }
    const rid = runtime_id orelse return null;
    const runtime = graph.getNode(rid).?;
    const children = try childrenOf(alloc, graph, runtime.id);
    return RuntimeView{
        .runtime = runtime,
        .indexes = try byKind(alloc, children, "index"),
        .streams = try byKind(alloc, children, "stream"),
        .fibers = try byKind(alloc, children, "fiber"),
        .surfaces = try byKind(alloc, children, "surface"),
        .parallels = try byKind(alloc, children, "parallel"),
    };
}

/// `_index_emit_targets`: the dotted `emit` target refs of an index node.
fn indexEmitTargets(alloc: Alloc, index_node: *const GraphNode) ![]const []const u8 {
    var out: std.ArrayList([]const u8) = .empty;
    const emit = nodeProp(index_node, "emit") orelse return out.toOwnedSlice(alloc);
    switch (emit) {
        .array => |items| {
            for (items) |x| {
                if (refOf(x)) |r| try out.append(alloc, r);
            }
        },
        else => {},
    }
    return out.toOwnedSlice(alloc);
}

fn hasTarget(targets: []const []const u8, t: []const u8) bool {
    for (targets) |x| {
        if (std.mem.eql(u8, x, t)) return true;
    }
    return false;
}

/// `_index_is_pinned`: True when the index declares `trust = pinned { … }`.
fn indexIsPinned(index_node: *const GraphNode) bool {
    const trust = nodeProp(index_node, "trust") orelse return false;
    switch (trust) {
        .object => {
            if (field(trust, "ref")) |rv| switch (rv) {
                .string => |s| return std.mem.eql(u8, s, "pinned"),
                else => {},
            };
            return false;
        },
        else => return false,
    }
}

fn fiberEngineName(fiber_node: *const GraphNode) ?[]const u8 {
    return refOf(nodeProp(fiber_node, "engine"));
}

