//! vaked-lower — the 0012 lowering pass: validated graph -> artifacts.
//!
//! Faithful port of `vakedc/lower.py`. Turns a *validated* typed semantic graph
//! (the output of `vakedc.check`) into the inspectable artifacts Vaked owns plus
//! the Nix spine that wires them, together with a decl-level provenance manifest.
//!
//! Purity / determinism (0012 §2): every emitter is a pure function of
//! `(graph, nodes)`. No IO, no clock, no randomness. All ordering is by a stable
//! graph-derived key (declaration source order via `edgesFrom`, the fixed
//! structural flake layout, lexicographic order for the provenance `artifacts`
//! map). The only IO is the CLI write layer (`cli/main.zig`), which writes the
//! `files` this module returns.
//!
//! SQLite is out of scope (no `catalog.sqlite` emitter — it is a deferred no-op,
//! exactly as in Python's fixture set).

