/// `_nix_source_slug`: the repo's last path segment, with `.`/`_` → `-`.
fn nixSourceSlug(alloc: Alloc, dotted: []const u8) ![]const u8 {
    const repo = if (std.mem.lastIndexOfScalar(u8, dotted, '/')) |i| dotted[i + 1 ..] else dotted;
    const slug = try alloc.dupe(u8, repo);
    for (slug) |*c| {
        if (c.* == '.' or c.* == '_') c.* = '-';
    }
    return slug;
}

/// Render `source` prop into a flat list of source app-calls (handles both the
/// single-app and list-of-apps shapes).
fn sourceApps(alloc: Alloc, src: ?Value) ![]const Value {
    var out: std.ArrayList(Value) = .empty;
    const s = src orelse return out.toOwnedSlice(alloc);
    switch (s) {
        .array => |items| {
            for (items) |x| try out.append(alloc, x);
        },
        else => try out.append(alloc, s),
    }
    return out.toOwnedSlice(alloc);
}

const EmitOut = struct { files: Files, entries: []const ProvEntry };

fn emitNixSpine(alloc: Alloc, graph: *Graph, rv: RuntimeView) !EmitOut {
    const runtime = rv.runtime;
    const sf = graph.source_file;
    const rt_name = runtime.name;

    const systems = try strList(alloc, nodeProp(runtime, "systems"));

    var lines: std.ArrayList([]const u8) = .empty;
    const A = struct {
        fn add(l: *std.ArrayList([]const u8), a: Alloc, s: []const u8) !void {
            try l.append(a, s);
        }
        fn addf(l: *std.ArrayList([]const u8), a: Alloc, comptime fmt: []const u8, args: anytype) !void {
            try l.append(a, try std.fmt.allocPrint(a, fmt, args));
        }
    };

    try A.addf(&lines, alloc, "# {s}", .{try header(alloc, sf, try std.fmt.allocPrint(alloc, "runtime {s}", .{rt_name}))});
    try A.add(&lines, alloc, "#");
    try A.add(&lines, alloc, "# Expected-output fixture (no compiler exists yet) \u{2014} see ./README.md and");
    try A.add(&lines, alloc, "# docs/language/0012-lowering.md \u{a7}4 (the Nix spine). Edits belong in the source");
    try A.add(&lines, alloc, "# .vaked file, not here.");
    try A.add(&lines, alloc, "{");
    try A.addf(&lines, alloc, "  description = \"{s} \u{2014} generated by Vaked\";", .{rt_name});
    try A.add(&lines, alloc, "");
    try A.add(&lines, alloc, "  inputs = {");
    try A.add(&lines, alloc, "    # nixpkgs is emitted pinned to the toolchain's baseline rev (0012 \u{a7}4.1): an");
    try A.add(&lines, alloc, "    # explicit rev, never a moving channel ref. The 40-hex value below is a");
    try A.add(&lines, alloc, "    # disclosed placeholder (all-`b` = \"baseline\"; see ./README.md); the");
    try A.add(&lines, alloc, "    # committed flake.lock (produced at first build) records the real resolution.");
    try A.addf(&lines, alloc, "    nixpkgs.url = \"github:NixOS/nixpkgs/{s}\";", .{NIXPKGS_BASELINE_REV});

    for (rv.indexes) |idx| {
        const src = nodeProp(idx, "source");
        if (indexIsPinned(idx)) {
            const call = try appCall(alloc, src);
            var owner: []const u8 = "";
            if (call) |c| {
                if (c.args.len > 0) owner = c.args[0] orelse "";
            }
            try A.add(&lines, alloc, "");
            try A.addf(&lines, alloc, "    # index {s} \u{2014} trust = pinned {{ commit, sha256 }} (0012 \u{a7}4.2):", .{idx.name});
            try A.add(&lines, alloc, "    # commit pins the rev; sha256 is recorded as the lock entry's narHash so the");
            try A.add(&lines, alloc, "    # build verifies the fetch. raw.github(...) => flake = false.");
            try A.addf(&lines, alloc, "    {s}-src = {{", .{idx.name});
            try A.addf(&lines, alloc, "      url = \"github:{s}/<commit>\"; # trust.pinned.commit", .{owner});
            try A.add(&lines, alloc, "      flake = false;");
            try A.add(&lines, alloc, "    };");
        } else {
            const sources = try sourceApps(alloc, src);
            try A.add(&lines, alloc, "");
            try A.addf(&lines, alloc, "    # index {s} \u{2014} sources (unpinned; flake.lock records the resolved rev).", .{idx.name});
            try A.add(&lines, alloc, "    # 0012 \u{a7}4.2: each index source becomes a flake input.");
            for (sources) |s| {
                const call = try appCall(alloc, s);
                if (call == null) continue;
                const owner_repo = if (call.?.args.len > 0) (call.?.args[0] orelse "") else "";
                const slug = try nixSourceSlug(alloc, owner_repo);
                try A.addf(&lines, alloc, "    {s}-src-{s} = {{ url = \"github:{s}\"; flake = false; }};", .{ idx.name, slug, owner_repo });
            }
        }
    }
    try A.add(&lines, alloc, "  };");
    try A.add(&lines, alloc, "");
    try A.add(&lines, alloc, "  outputs = { self, nixpkgs, ... }@inputs:");
    try A.add(&lines, alloc, "    let");
    {
        // "      # runtime <rt> — systems = [\"a\", \"b\"]"
        var sysq: Buf = .empty;
        for (systems, 0..) |s, i| {
            if (i != 0) try sysq.appendSlice(alloc, ", ");
            try sysq.appendSlice(alloc, try std.fmt.allocPrint(alloc, "\"{s}\"", .{s}));
        }
        try A.addf(&lines, alloc, "      # runtime {s} \u{2014} systems = [{s}]", .{ rt_name, sysq.items });
    }
    {
        // "      systems = [ \"a\" \"b\" ];"  (space-joined, no commas)
        var sysn: Buf = .empty;
        for (systems, 0..) |s, i| {
            if (i != 0) try sysn.appendSlice(alloc, " ");
            try sysn.appendSlice(alloc, try std.fmt.allocPrint(alloc, "\"{s}\"", .{s}));
        }
        try A.addf(&lines, alloc, "      systems = [ {s} ];", .{sysn.items});
    }
    try A.add(&lines, alloc, "      forAllSystems = f: nixpkgs.lib.genAttrs systems (system: f system);");
    try A.add(&lines, alloc, "    in");
    try A.add(&lines, alloc, "    {");
    try A.add(&lines, alloc, "      # nixosModules.<runtime> \u{2014} wires the OTP/Zig daemons and references the");
    try A.add(&lines, alloc, "      # gen/ artifacts as installed files (0012 \u{a7}4.3).");
    try A.addf(&lines, alloc, "      nixosModules.{s} = import ./nixos/{s}.nix {{", .{ rt_name, rt_name });
    try A.add(&lines, alloc, "        # NixOS module fixture is described in 0012 \u{a7}4.3; not emitted as a");
    try A.add(&lines, alloc, "        # separate file in this fixture set (interface only).");
    try A.add(&lines, alloc, "        inherit self;");
    try A.add(&lines, alloc, "      };");
    try A.add(&lines, alloc, "");
    try A.add(&lines, alloc, "      packages = forAllSystems (system:");
    try A.add(&lines, alloc, "        let pkgs = nixpkgs.legacyPackages.${system};");
    try A.add(&lines, alloc, "        in {");

    // packages: engines (from fibers, source order), then crabcc index derivations.
    var seen_engines: std.StringArrayHashMapUnmanaged(void) = .empty;
    for (rv.fibers) |fib| {
        const eng = fiberEngineName(fib) orelse continue;
        if (seen_engines.contains(eng)) continue;
        try seen_engines.put(alloc, eng, {});
        try A.addf(&lines, alloc, "          # engine {s} (fiber {s}: engine = {s}) \u{2014} built Zig pkg.", .{ eng, fib.name, eng });
        try A.addf(&lines, alloc, "          {s} = pkgs.callPackage ./pkgs/{s}.nix {{ }};", .{ eng, eng });
        try A.add(&lines, alloc, "");
    }
    const any_engine = seen_engines.count() > 0;

    for (rv.indexes) |idx| {
        const targets = try indexEmitTargets(alloc, idx);
        if (!hasTarget(targets, "nix.derivation")) continue;
        const normalize = refOf(nodeProp(idx, "normalize"));
        // cat_targets = [t for t in targets if t.startswith("catalog.")]
        var cat_targets: std.ArrayList([]const u8) = .empty;
        for (targets) |t| {
            if (std.mem.startsWith(u8, t, "catalog.")) try cat_targets.append(alloc, t);
        }
        // emit_flags = " ".join("--emit " + t.split(".",1)[1] ...)
        var emit_flags: Buf = .empty;
        for (cat_targets.items, 0..) |t, i| {
            if (i != 0) try emit_flags.appendSlice(alloc, " ");
            const after = afterDot(t);
            try emit_flags.appendSlice(alloc, try std.fmt.allocPrint(alloc, "--emit {s}", .{after}));
        }
        try A.addf(&lines, alloc, "          # index {s}, emit \u{220b} nix.derivation (0012 \u{a7}5.3a) \u{2014} CrabCC index", .{idx.name});
        try A.add(&lines, alloc, "          # derivation; runs crabcc at build time over the pinned sources with");
        try A.addf(&lines, alloc, "          # normalize = {s}.", .{normalize orelse ""});
        try A.addf(&lines, alloc, "          {s}-crabcc-index = pkgs.stdenv.mkDerivation {{", .{idx.name});
        try A.addf(&lines, alloc, "            pname = \"{s}-crabcc-index\";", .{idx.name});
        try A.add(&lines, alloc, "            version = \"0\";");
        try A.add(&lines, alloc, "            srcs = [");
        const sources = try sourceApps(alloc, nodeProp(idx, "source"));
        for (sources) |s| {
            const call = try appCall(alloc, s);
            if (call == null) continue;
            const owner_repo = if (call.?.args.len > 0) (call.?.args[0] orelse "") else "";
            const slug = try nixSourceSlug(alloc, owner_repo);
            try A.addf(&lines, alloc, "              inputs.{s}-src-{s}", .{ idx.name, slug });
        }
        try A.add(&lines, alloc, "            ];");
        try A.add(&lines, alloc, "            nativeBuildInputs = [ pkgs.crabcc ];");
        try A.add(&lines, alloc, "            buildPhase = ''");
        // norm_arg = normalize.split(".",1)[1] if normalize has "." else normalize
        const norm_arg: []const u8 = blk: {
            if (normalize) |n| {
                if (std.mem.indexOfScalar(u8, n, '.') != null) break :blk afterDot(n);
                break :blk n;
            }
            break :blk "";
        };
        // "# normalize = <full> ; emit = <cat_targets joined ', '>"
        var cat_join: Buf = .empty;
        for (cat_targets.items, 0..) |t, i| {
            if (i != 0) try cat_join.appendSlice(alloc, ", ");
            try cat_join.appendSlice(alloc, t);
        }
        try A.addf(&lines, alloc, "              # normalize = {s} ; emit = {s}", .{ normalize orelse "", cat_join.items });
        try A.addf(&lines, alloc, "              crabcc index build --normalize {s} \\", .{norm_arg});
        try A.addf(&lines, alloc, "                {s} \\", .{emit_flags.items});
        try A.add(&lines, alloc, "                --out $out");
        try A.add(&lines, alloc, "            '';");
        try A.add(&lines, alloc, "          };");
    }
    try A.add(&lines, alloc, "        });");
    try A.add(&lines, alloc, "");
    try A.add(&lines, alloc, "      apps = forAllSystems (system:");
    try A.add(&lines, alloc, "        let pkgs = nixpkgs.legacyPackages.${system};");
    try A.add(&lines, alloc, "        in {");
    for (rv.surfaces) |surf| {
        const mode = refOf(nodeProp(surf, "mode"));
        try A.addf(&lines, alloc, "          # surface {s} (mode = {s}) \u{2014} launcher app.", .{ surf.name, mode orelse "" });
