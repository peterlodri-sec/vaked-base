/// "`s`"
fn mdCode(alloc: Alloc, s: []const u8) ![]const u8 {
    return std.fmt.allocPrint(alloc, "`{s}`", .{s});
}

/// Render an index's source(s) for the Indexes table: comma-joined code spans of
/// `ref("arg", ...)`.
fn indexSourceRender(alloc: Alloc, idx: *const GraphNode) ![]const u8 {
    const sources = try sourceApps(alloc, nodeProp(idx, "source"));
    var parts: std.ArrayList([]const u8) = .empty;
    for (sources) |s| {
        const call = try appCall(alloc, s);
        if (call) |c| {
            var argbuf: Buf = .empty;
            for (c.args, 0..) |a, i| {
                if (i != 0) try argbuf.appendSlice(alloc, ", ");
                try argbuf.appendSlice(alloc, try std.fmt.allocPrint(alloc, "\"{s}\"", .{a orelse ""}));
            }
            const rendered = try std.fmt.allocPrint(alloc, "{s}({s})", .{ c.ref, argbuf.items });
            try parts.append(alloc, try mdCode(alloc, rendered));
        }
    }
    return joinComma(alloc, parts.items);
}

fn joinComma(alloc: Alloc, parts: []const []const u8) ![]const u8 {
    var buf: Buf = .empty;
    for (parts, 0..) |p, i| {
        if (i != 0) try buf.appendSlice(alloc, ", ");
        try buf.appendSlice(alloc, p);
    }
    return buf.toOwnedSlice(alloc);
}

