/// An ordered JSON object: `[(key, value)]` preserving the §5.2 schema order.
const Ordered = struct {
    pairs: []const Pair,
    const Pair = struct { key: []const u8, value: ZVal };
};

/// A zig-config JSON value: scalar `Value`, ordered object, or array.
const ZVal = union(enum) {
    scalar: Value,
    ordered: Ordered,
    array: []const ZVal,
};

fn streamForFiberInput(rv: RuntimeView, fiber: *const GraphNode) struct { st: ?*GraphNode, inp: ?[]const u8 } {
    const inp = refOf(nodeProp(fiber, "input"));
    if (inp == null) return .{ .st = null, .inp = null };
    // last segment of dotted input
    const target_name = if (std.mem.lastIndexOfScalar(u8, inp.?, '.')) |i| inp.?[i + 1 ..] else inp.?;
    for (rv.streams) |st| {
        if (std.mem.eql(u8, st.name, target_name)) return .{ .st = st, .inp = inp };
    }
    return .{ .st = null, .inp = inp };
}

