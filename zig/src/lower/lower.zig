/// `_config_block_name`: the field name of a bare config-block App (`policy {…}`):
/// a single-segment ref with a record body and no call args. Else null.
fn configBlockName(app: ast.App) ?[]const u8 {
    if (app.args != null or app.record == null) return null;
    if (app.ref.parts.len != 1) return null;
    return app.ref.parts[0];
}

fn isConfigBlockAllowed(decl_kind: []const u8, name: []const u8) bool {
    // _CONFIG_BLOCK_FIELDS = {"fiber": {"policy"}}
    return std.mem.eql(u8, decl_kind, "fiber") and std.mem.eql(u8, name, "policy");
}

/// `_value_to_props` for the policy-block App. Mirrors `resolve.py:_value_to_props`
/// for an App: `{"ref": dotted, "record": [entries]}` (the App branch keeps the
/// `ref` — for `policy { … }` that is `"policy"`). The `ref` field is
/// load-bearing: the §6.2 projection hash is computed over it.
fn appRecordToProps(alloc: Alloc, app: ast.App) !Value {
    return exprToProps(alloc, .{ .app = app });
}

fn entriesToProps(alloc: Alloc, entries: []const ast.Entry) !Value {
    var arr = try alloc.alloc(Value, entries.len);
    for (entries, 0..) |e, i| arr[i] = try entryToProps(alloc, e);
    return .{ .array = arr };
}

fn entryToProps(alloc: Alloc, e: ast.Entry) !Value {
    switch (e) {
        .assignment => |a| {
            const fields = try alloc.dupe(Value.Field, &.{
                .{ .key = "assign", .value = .{ .string = a.target } },
                .{ .key = "op", .value = .{ .string = a.op } },
                .{ .key = "value", .value = try exprToProps(alloc, a.value) },
            });
            return .{ .object = fields };
        },
        .inherit => |ih| {
            var arr = try alloc.alloc(Value, ih.names.len);
            for (ih.names, 0..) |nm, i| arr[i] = .{ .string = nm };
            const fields = try alloc.dupe(Value.Field, &.{.{ .key = "inherit", .value = .{ .array = arr } }});
            return .{ .object = fields };
        },
    }
}

/// `_value_to_props` for an expression. Mirrors resolve.py exactly.
fn exprToProps(alloc: Alloc, v: ast.Expr) (Alloc.Error)!Value {
    switch (v) {
        .literal => |lit| {
            const fields = try alloc.dupe(Value.Field, &.{
                .{ .key = "lit", .value = .{ .string = lit.kind.lower() } },
                .{ .key = "value", .value = .{ .string = lit.value } },
            });
            return .{ .object = fields };
        },
        .list => |ll| {
            var arr = try alloc.alloc(Value, ll.items.len);
            for (ll.items, 0..) |x, i| arr[i] = try exprToProps(alloc, x);
            return .{ .array = arr };
        },
        .record => |rl| {
            const fields = try alloc.dupe(Value.Field, &.{.{ .key = "record", .value = try entriesToProps(alloc, rl.entries) }});
            return .{ .object = fields };
        },
        .app => |a| {
            var fields: std.ArrayList(Value.Field) = .empty;
            try fields.append(alloc, .{ .key = "ref", .value = .{ .string = try a.ref.dotted(alloc) } });
            if (a.args) |args| {
                var arr = try alloc.alloc(Value, args.len);
                for (args, 0..) |x, i| arr[i] = try exprToProps(alloc, x);
                try fields.append(alloc, .{ .key = "args", .value = .{ .array = arr } });
            }
            if (a.record) |rec| {
                try fields.append(alloc, .{ .key = "record", .value = try entriesToProps(alloc, rec) });
            }
            return .{ .object = try fields.toOwnedSlice(alloc) };
        },
    }
}

/// Append a `policy` prop to a node's `props` object (the node props is a
/// `Value.object`). Returns a new object Value with the field appended.
fn objectWithField(alloc: Alloc, obj: Value, key: []const u8, value: Value) !Value {
    var fields: std.ArrayList(Value.Field) = .empty;
    switch (obj) {
        .object => |existing| {
            for (existing) |f| try fields.append(alloc, f);
        },
        else => {},
    }
    try fields.append(alloc, .{ .key = key, .value = value });
    return .{ .object = try fields.toOwnedSlice(alloc) };
}

/// `_node_for_chain`: find the graph node whose id ends with the decl-name chain.
fn nodeForChain(alloc: Alloc, graph: *Graph, chain: []const []const u8) !?*GraphNode {
    const joined = try std.mem.join(alloc, "/", chain);
    const suffix = try std.fmt.allocPrint(alloc, "#{s}", .{joined});
    var it = graph.nodes.iterator();
    while (it.next()) |entry| {
        const n = entry.value_ptr;
        if (std.mem.endsWith(u8, n.id, suffix) and n.provenance != null) return n;
    }
    return null;
}

const EnrichError = error{OutOfMemory};

/// `enrich_graph`: attach dropped config sub-blocks (a fiber's `policy { … }`) to
/// their graph nodes, in place. Idempotent; adds no nodes/edges.
fn enrichGraph(alloc: Alloc, graph: *Graph, items: []const ast.Item) EnrichError!void {
    for (items) |it| {
        switch (it) {
            .decl => |d| try enrichWalk(alloc, graph, d, &.{d.name}),
            .import => {},
        }
    }
}

fn enrichWalk(alloc: Alloc, graph: *Graph, decl: ast.Decl, chain: []const []const u8) EnrichError!void {
    if (try nodeForChain(alloc, graph, chain)) |node| {
        for (decl.body) |st| {
            switch (st) {
                .app => |app| {
                    if (configBlockName(app)) |name| {
                        if (isConfigBlockAllowed(decl.kind, name) and !hasField(node.props, name)) {
                            const pv = try appRecordToProps(alloc, app);
                            node.props = try objectWithField(alloc, node.props, name, pv);
                        }
                    }
                },
                else => {},
            }
        }
    }
    for (decl.body) |st| {
        switch (st) {
            .decl => |child| {
                const new_chain = try alloc.alloc([]const u8, chain.len + 1);
                @memcpy(new_chain[0..chain.len], chain);
                new_chain[chain.len] = child.name;
                try enrichWalk(alloc, graph, child, new_chain);
            },
            else => {},
        }
    }
}

