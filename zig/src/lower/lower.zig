/// `s.split(".", 1)[1]` — everything after the first dot (or `s` if no dot).
fn afterDot(s: []const u8) []const u8 {
    if (std.mem.indexOfScalar(u8, s, '.')) |i| return s[i + 1 ..];
    return s;
}

/// "\n".join(lines) + "\n"
fn joinLines(alloc: Alloc, lines: []const []const u8) ![]u8 {
    var buf: Buf = .empty;
    for (lines) |l| {
        try buf.appendSlice(alloc, l);
        try buf.append(alloc, '\n');
    }
    return buf.toOwnedSlice(alloc);
}

