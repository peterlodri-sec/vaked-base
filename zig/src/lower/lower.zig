/// The source-file name as it appears in the §6.1 generated header: the
/// *basename* (`operator-field.vaked`), never the full path.
fn headerFile(alloc: Alloc, source_file: []const u8) ![]const u8 {
    // posixpath.basename(source_file.replace("\\", "/"))
    const norm = try alloc.dupe(u8, source_file);
    for (norm) |*c| {
        if (c.* == '\\') c.* = '/';
    }
    if (std.mem.lastIndexOfScalar(u8, norm, '/')) |i| return norm[i + 1 ..];
    return norm;
}

fn header(alloc: Alloc, source_file: []const u8, decl: []const u8) ![]const u8 {
    const f = try headerFile(alloc, source_file);
    return std.fmt.allocPrint(alloc, "generated by Vaked from {s}:{s} \u{2014} do not edit", .{ f, decl });
}

// --------------------------------------------------------------------------- //
// Value accessors — mirror lower.py's _ref / _lit / _str_list / _app_call /
// _record_entries, reading the JSON-shaped prop `Value`.
// --------------------------------------------------------------------------- //

/// The value of object field `key`, or null.
fn field(v: ?Value, key: []const u8) ?Value {
    const obj = v orelse return null;
    switch (obj) {
        .object => |fields| {
            for (fields) |f| {
                if (std.mem.eql(u8, f.key, key)) return f.value;
            }
            return null;
        },
        else => return null,
    }
}

fn hasField(v: Value, key: []const u8) bool {
    return field(v, key) != null;
}

fn nodeProp(node: *const GraphNode, key: []const u8) ?Value {
    return field(node.props, key);
}

/// `_ref`: the dotted ref string of a `{"ref": "..."}` prop (no `args`, no
/// `record`), else null.
fn refOf(prop: ?Value) ?[]const u8 {
    const p = prop orelse return null;
    switch (p) {
        .object => {
            if (hasField(p, "ref") and !hasField(p, "args") and !hasField(p, "record")) {
                if (field(p, "ref")) |rv| switch (rv) {
                    .string => |s| return s,
                    else => {},
                };
            }
            return null;
        },
        else => return null,
    }
}

/// `_lit`: the literal value of a `{"lit": ..., "value": ...}` prop, else null.
/// Returns the `value` Value (a string in the corpus).
fn litOf(prop: ?Value) ?Value {
    const p = prop orelse return null;
    switch (p) {
        .object => {
            if (hasField(p, "lit")) return field(p, "value");
            return null;
        },
        else => return null,
    }
}

/// `_lit` as a string (the corpus always stores lit values as strings).
fn litStr(prop: ?Value) ?[]const u8 {
    const v = litOf(prop) orelse return null;
    switch (v) {
        .string => |s| return s,
        else => return null,
    }
}

/// `_str_list`: the string-literal values of a list prop.
fn strList(alloc: Alloc, prop: ?Value) ![]const []const u8 {
    var out: std.ArrayList([]const u8) = .empty;
    const p = prop orelse return out.toOwnedSlice(alloc);
    switch (p) {
        .array => |items| {
            for (items) |x| {
                if (litStr(x)) |s| try out.append(alloc, s);
            }
        },
        else => {},
    }
    return out.toOwnedSlice(alloc);
}

const AppCall = struct { ref: []const u8, args: []const ?[]const u8 };

/// `_app_call`: if `prop` is an application `f(args...)`, return (ref, [arg-lits]).
fn appCall(alloc: Alloc, prop: ?Value) !?AppCall {
    const p = prop orelse return null;
    switch (p) {
        .object => {
            if (hasField(p, "ref") and hasField(p, "args")) {
                const ref_v = field(p, "ref").?;
                const ref_s = switch (ref_v) {
                    .string => |s| s,
                    else => return null,
                };
                var args: std.ArrayList(?[]const u8) = .empty;
                const args_v = field(p, "args").?;
                switch (args_v) {
                    .array => |items| {
                        for (items) |a| try args.append(alloc, litStr(a));
                    },
                    else => {},
                }
                return AppCall{ .ref = ref_s, .args = try args.toOwnedSlice(alloc) };
            }
            return null;
        },
        else => return null,
    }
}

const RecordEntry = struct { name: []const u8, value: ?Value };

/// `_record_entries`: the `[{"assign","op","value"}]` entries of a
/// record/record-app prop, as `name -> value-literal` pairs (preserving order).
/// The returned `value` is the *literal value* (mirroring `_lit(e.value)`).
fn recordEntries(alloc: Alloc, prop: ?Value) ![]const RecordEntry {
    var out: std.ArrayList(RecordEntry) = .empty;
    const p = prop orelse return out.toOwnedSlice(alloc);
    const rec = field(p, "record") orelse return out.toOwnedSlice(alloc);
    switch (rec) {
        .array => |items| {
            for (items) |e| {
                if (field(e, "assign")) |assign_v| {
                    const name = switch (assign_v) {
                        .string => |s| s,
                        else => continue,
                    };
                    try out.append(alloc, .{ .name = name, .value = litOf(field(e, "value")) });
                }
            }
        },
        else => {},
    }
    return out.toOwnedSlice(alloc);
}

// --------------------------------------------------------------------------- //
// Canonical projection JSON + inputsHash (0012 §6.2).
// --------------------------------------------------------------------------- //

/// The canonical projection JSON a node/engine projection is hashed over: sorted
/// keys, compact separators, ensure_ascii=False, no trailing newline. Reuses the
/// core compact writer (sorts object keys recursively).
fn canonicalProjectionJson(alloc: Alloc, projection: Value) ![]u8 {
    var buf: Buf = .empty;
    try json_canon.writeValueCompact(&buf, alloc, projection);
    return buf.toOwnedSlice(alloc);
}

fn inputsHash(alloc: Alloc, projection: Value) ![]const u8 {
    const canonical = try canonicalProjectionJson(alloc, projection);
    var digest: [32]u8 = undefined;
    std.crypto.hash.sha2.Sha256.hash(canonical, &digest, .{});
    const hex = std.fmt.bytesToHex(digest, .lower);
    return std.fmt.allocPrint(alloc, "sha256-{s}", .{hex});
}

/// A node's projection: kind + name + (canonicalized) props. The compact writer
/// sorts keys at emit time, so we hand props through verbatim.
fn nodeProjection(alloc: Alloc, node: *const GraphNode) !Value {
    const fields = try alloc.dupe(Value.Field, &.{
        .{ .key = "kind", .value = .{ .string = node.kind } },
        .{ .key = "name", .value = .{ .string = node.name } },
        .{ .key = "props", .value = node.props },
    });
    return .{ .object = fields };
}

/// The resolved-engine projection for a fiber's `packages.<engine>` region.
fn engineProjection(alloc: Alloc, engine_name: []const u8) !Value {
    const pkg = try std.fmt.allocPrint(alloc, "packages.{s}", .{engine_name});
    const fields = try alloc.dupe(Value.Field, &.{
        .{ .key = "engine", .value = .{ .string = engine_name } },
        .{ .key = "package", .value = .{ .string = pkg } },
    });
    return .{ .object = fields };
}

