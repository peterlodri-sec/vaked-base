const CatalogRow = struct { owner_repo: []const u8, path: []const u8, text: []const u8 };

/// Placeholder catalog rows keyed by index name (0012 §5.3b).
fn catalogPlaceholderRows(name: []const u8) []const CatalogRow {
    if (std.mem.eql(u8, name, "zigCorpus")) {
        const rows = struct {
            const r = [_]CatalogRow{
                .{ .owner_repo = "Sobeston/zig.guide", .path = "chapter-1/hello-world.md", .text = "# Hello World\n\nCreate a file `hello.zig` and run it with `zig run hello.zig`." },
                .{ .owner_repo = "zigimg/zigimg", .path = "README.md", .text = "# zigimg\n\nZig library for reading and writing images in a variety of formats." },
            };
        };
        return &rows.r;
    }
    return &.{};
}

fn catalogRowId(alloc: Alloc, owner_repo: []const u8, n: usize) ![]const u8 {
    const repo = if (std.mem.lastIndexOfScalar(u8, owner_repo, '/')) |i| owner_repo[i + 1 ..] else owner_repo;
    return std.fmt.allocPrint(alloc, "{s}#{d:0>4}", .{ repo, n });
}

/// Compact JSON object in INSERTION order (no key sorting) — mirrors Python's
/// `json.dumps(obj, separators=(",",":"))` over an ordered dict (the catalog
/// header + rows, 0012 §5.3b). Values are scalars here, written via the core
/// compact writer.
fn writeCompactOrdered(buf: *Buf, alloc: Alloc, fields: []const Value.Field) WriteError!void {
    try buf.append(alloc, '{');
    for (fields, 0..) |f, i| {
        if (i != 0) try buf.append(alloc, ',');
        try json_canon.writeJsonString(buf, alloc, f.key);
        try buf.append(alloc, ':');
        try json_canon.writeValueCompact(buf, alloc, f.value);
    }
    try buf.append(alloc, '}');
}

fn emitCatalogJsonl(alloc: Alloc, graph: *Graph, nodes: []*GraphNode) !EmitOut {
    const sf = graph.source_file;
    var files: Files = .empty;
    var entries: std.ArrayList(ProvEntry) = .empty;
    for (nodes) |idx| {
        var lines: std.ArrayList([]const u8) = .empty;
        // header object — compact JSON: {"_generated":"..."}
        const hdr = try header(alloc, sf, try std.fmt.allocPrint(alloc, "index {s}", .{idx.name}));
        const hdr_fields = try alloc.dupe(Value.Field, &.{.{ .key = "_generated", .value = .{ .string = hdr } }});
        var hb: Buf = .empty;
        try writeCompactOrdered(&hb, alloc, hdr_fields);
        try lines.append(alloc, try hb.toOwnedSlice(alloc));

        const rows = catalogPlaceholderRows(idx.name);
        var per_repo: std.StringArrayHashMapUnmanaged(usize) = .empty;
        for (rows) |row| {
            const repo = if (std.mem.lastIndexOfScalar(u8, row.owner_repo, '/')) |i| row.owner_repo[i + 1 ..] else row.owner_repo;
            const gop = try per_repo.getOrPut(alloc, repo);
            if (!gop.found_existing) gop.value_ptr.* = 0;
            gop.value_ptr.* += 1;
            const n = gop.value_ptr.*;
            // Build the row object in the §5.3b key order: id, source, path,
            // chunk, text — emitted in INSERTION order (Python's json.dumps over
            // an ordered dict; NOT sorted).
            const id = try catalogRowId(alloc, row.owner_repo, n);
            const source = try std.fmt.allocPrint(alloc, "github:{s}", .{row.owner_repo});
            const row_fields = try alloc.dupe(Value.Field, &.{
                .{ .key = "id", .value = .{ .string = id } },
                .{ .key = "source", .value = .{ .string = source } },
                .{ .key = "path", .value = .{ .string = row.path } },
                .{ .key = "chunk", .value = .{ .int = 0 } },
                .{ .key = "text", .value = .{ .string = row.text } },
            });
            var rb: Buf = .empty;
            try writeCompactOrdered(&rb, alloc, row_fields);
            try lines.append(alloc, try rb.toOwnedSlice(alloc));
        }
        const text = try joinLines(alloc, lines.items);
        const path = try std.fmt.allocPrint(alloc, "gen/catalog/{s}.jsonl", .{idx.name});
        try files.put(alloc, path, text);
        try entries.append(alloc, .{
            .artifact = path,
            .region = null,
            .source_file = sf,
            .decl = try std.fmt.allocPrint(alloc, "index {s}", .{idx.name}),
            .span = idx.provenance.?.span,
            .emitter = "catalog.jsonl",
            .inputs_projection = try nodeProjection(alloc, idx),
        });
    }
    return .{ .files = files, .entries = try entries.toOwnedSlice(alloc) };
}

