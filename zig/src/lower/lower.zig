        const cfg = try zigConfigForFiber(alloc, rv, fib, sf);
        const text = try emitZigJson(alloc, cfg);
        const path = try std.fmt.allocPrint(alloc, "gen/zig/{s}.json", .{fib.name});
        try files.put(alloc, path, text);
        try entries.append(alloc, .{
            .artifact = path,
            .region = null,
            .source_file = sf,
            .decl = try std.fmt.allocPrint(alloc, "fiber {s}", .{fib.name}),
            .span = fib.provenance.?.span,
            .emitter = "zig.daemoncfg",
            .inputs_projection = try nodeProjection(alloc, fib),
        });
    }
    return .{ .files = files, .entries = try entries.toOwnedSlice(alloc) };
}

fn zigConfigForFiber(alloc: Alloc, rv: RuntimeView, fib: *const GraphNode, sf: []const u8) !Ordered {
    const eng = fiberEngineName(fib);
    const si = streamForFiberInput(rv, fib);

    var pairs: std.ArrayList(Ordered.Pair) = .empty;
    try pairs.append(alloc, .{ .key = "_generated", .value = .{ .scalar = .{ .string = try header(alloc, sf, try std.fmt.allocPrint(alloc, "fiber {s}", .{fib.name})) } } });
    if (eng) |e| {
        try pairs.append(alloc, .{ .key = "engine", .value = .{ .scalar = .{ .string = e } } });
        try pairs.append(alloc, .{ .key = "engine_package", .value = .{ .scalar = .{ .string = try std.fmt.allocPrint(alloc, "packages.{s}", .{e}) } } });
    }

    // input { stream, source, type, fps }
    if (si.st) |st| {
        var ip: std.ArrayList(Ordered.Pair) = .empty;
        try ip.append(alloc, .{ .key = "stream", .value = .{ .scalar = .{ .string = st.name } } });
        if (refOf(nodeProp(st, "source"))) |s| try ip.append(alloc, .{ .key = "source", .value = .{ .scalar = .{ .string = s } } });
        if (refOf(nodeProp(st, "type"))) |t| try ip.append(alloc, .{ .key = "type", .value = .{ .scalar = .{ .string = t } } });
        if (litStr(nodeProp(st, "fps"))) |f| try ip.append(alloc, .{ .key = "fps", .value = .{ .scalar = coerceNumber(f) } });
        if (ip.items.len > 0) {
            try pairs.append(alloc, .{ .key = "input", .value = .{ .ordered = .{ .pairs = try ip.toOwnedSlice(alloc) } } });
        }
    }

    // output { target }
    if (refOf(nodeProp(fib, "output"))) |out_ref| {
        const op = try alloc.dupe(Ordered.Pair, &.{.{ .key = "target", .value = .{ .scalar = .{ .string = out_ref } } }});
        try pairs.append(alloc, .{ .key = "output", .value = .{ .ordered = .{ .pairs = op } } });
    }

    // policy { ... } — from the enriched policy sub-block.
    const policy_fields = try fiberPolicyFields(alloc, fib);
    if (policy_fields.len > 0) {
        var pp: std.ArrayList(Ordered.Pair) = .empty;
        for (policy_fields) |kv| {
            try pp.append(alloc, .{ .key = kv.name, .value = zvalOfScalar(alloc, kv.value) });
        }
        try pairs.append(alloc, .{ .key = "policy", .value = .{ .ordered = .{ .pairs = try pp.toOwnedSlice(alloc) } } });
    }

    // budget (optional) — omitted when absent.
    if (nodeProp(fib, "budget")) |budget| {
        try pairs.append(alloc, .{ .key = "budget", .value = .{ .scalar = try scalarProp(alloc, budget) } });
    }

    // observe (default false)
    if (nodeProp(fib, "observe")) |observe| {
        try pairs.append(alloc, .{ .key = "observe", .value = .{ .scalar = try scalarProp(alloc, observe) } });
    } else {
        try pairs.append(alloc, .{ .key = "observe", .value = .{ .scalar = .{ .bool = false } } });
    }

    return .{ .pairs = try pairs.toOwnedSlice(alloc) };
}

/// Wrap a scalar policy `Value` into a `ZVal` (lists become array of scalar ZVal).
fn zvalOfScalar(alloc: Alloc, v: Value) ZVal {
    switch (v) {
        .array => |items| {
            var out = alloc.alloc(ZVal, items.len) catch unreachable;
            for (items, 0..) |x, i| out[i] = .{ .scalar = x };
            return .{ .array = out };
        },
        else => return .{ .scalar = v },
    }
}

/// `_coerce_number`: a numeric string ("10") becomes int/float for JSON (10).
fn coerceNumber(value: []const u8) Value {
    // Float if it has '.', 'e', or 'E'; else int.
    if (std.mem.indexOfAny(u8, value, ".eE") != null) {
        if (std.fmt.parseFloat(f64, value)) |f| {
            return .{ .float = f };
        } else |_| {}
    } else {
        if (std.fmt.parseInt(i64, value, 10)) |n| {
            return .{ .int = n };
        } else |_| {}
    }
    return .{ .string = value };
}

/// `_scalar_prop`: project a recorded prop value to its scalar JSON value.
fn scalarProp(alloc: Alloc, raw: ?Value) !Value {
    const r = raw orelse return .null;
    switch (r) {
        .bool => return r,
        .object => {
            if (field(r, "lit")) |kind_v| {
                const kind = switch (kind_v) {
                    .string => |s| s,
                    else => return r,
                };
                const val = field(r, "value");
                if (std.mem.eql(u8, kind, "number")) {
                    if (val) |v| switch (v) {
                        .string => |s| return coerceNumber(s),
                        else => return v,
                    };
                    return .null;
                }
                if (std.mem.eql(u8, kind, "bool") or std.mem.eql(u8, kind, "boolean")) {
                    if (val) |v| switch (v) {
                        .string => |s| return .{ .bool = std.mem.eql(u8, s, "true") },
                        .bool => |b| return .{ .bool = b },
                        else => return .{ .bool = false },
                    };
                    return .{ .bool = false };
                }
                return val orelse .null;
            }
            if (refOf(r)) |rr| return .{ .string = rr };
            return r;
        },
        .array => |items| {
            var out = try alloc.alloc(Value, items.len);
            for (items, 0..) |x, i| out[i] = try scalarProp(alloc, x);
            return .{ .array = out };
        },
        else => return r,
    }
}

/// Serialize an `Ordered` config to the §5.2 JSON layout (2-space indent, one
/// member per line, inline scalar arrays), with a trailing newline.
fn emitZigJson(alloc: Alloc, obj: Ordered) ![]u8 {
    var buf: Buf = .empty;
    try emitZigVal(alloc, &buf, .{ .ordered = obj }, 0);
    try buf.append(alloc, '\n');
    return buf.toOwnedSlice(alloc);
}

fn emitZigVal(alloc: Alloc, buf: *Buf, val: ZVal, level: usize) WriteError!void {
    switch (val) {
        .ordered => |o| {
            if (o.pairs.len == 0) {
                try buf.appendSlice(alloc, "{}");
                return;
            }
            try buf.appendSlice(alloc, "{\n");
            for (o.pairs, 0..) |p, i| {
                try writeIndent(alloc, buf, level + 1);
                try json_canon.writeJsonString(buf, alloc, p.key);
                try buf.appendSlice(alloc, ": ");
                try emitZigVal(alloc, buf, p.value, level + 1);
                if (i != o.pairs.len - 1) try buf.append(alloc, ',');
                try buf.append(alloc, '\n');
            }
            try writeIndent(alloc, buf, level);
            try buf.append(alloc, '}');
        },
        .array => |items| {
            // one-line array of scalars
            try buf.append(alloc, '[');
            for (items, 0..) |x, i| {
                if (i != 0) try buf.appendSlice(alloc, ", ");
                try emitZigVal(alloc, buf, x, level + 1);
            }
            try buf.append(alloc, ']');
        },
        .scalar => |v| try json_canon.writeValueCompact(buf, alloc, v),
    }
}

fn writeIndent(alloc: Alloc, buf: *Buf, level: usize) !void {
    var i: usize = 0;
    while (i < level) : (i += 1) try buf.appendSlice(alloc, "  ");
}

