/// Build the provenance.json document (0012 §6.2). The `artifacts` map is keyed
/// lexicographically by artifact path; per-artifact entries preserve emission
/// order. Returns a `Value` whose object field order is the intended pretty-print
/// order (the pretty-printer preserves insertion order, not sorting).
fn buildProvenance(alloc: Alloc, source_file: []const u8, entries: []const ProvEntry) !Value {
    // Group entries by artifact, preserving first-seen order then sort the keys.
    var artifacts: std.StringArrayHashMapUnmanaged(std.ArrayList(ProvEntry)) = .empty;
    for (entries) |ent| {
        const gop = try artifacts.getOrPut(alloc, ent.artifact);
        if (!gop.found_existing) gop.value_ptr.* = .empty;
        try gop.value_ptr.append(alloc, ent);
    }

    // Sort artifact paths lexicographically.
    const keys = try alloc.dupe([]const u8, artifacts.keys());
    std.mem.sort([]const u8, keys, {}, lessStr);

    var art_fields: std.ArrayList(Value.Field) = .empty;
    for (keys) |path| {
        const arr_entries = artifacts.get(path).?;
        var out_entries = try alloc.alloc(Value, arr_entries.items.len);
        for (arr_entries.items, 0..) |ent, i| {
            var ef: std.ArrayList(Value.Field) = .empty;
            if (ent.region) |r| try ef.append(alloc, .{ .key = "region", .value = .{ .string = r } });
            try ef.append(alloc, .{ .key = "sourceFile", .value = .{ .string = ent.source_file } });
            try ef.append(alloc, .{ .key = "decl", .value = .{ .string = ent.decl } });
            // span object (in field order file, byteStart, byteEnd, line, col).
            const span_fields = try alloc.dupe(Value.Field, &.{
                .{ .key = "file", .value = .{ .string = ent.source_file } },
                .{ .key = "byteStart", .value = .{ .int = @intCast(ent.span.byteStart) } },
                .{ .key = "byteEnd", .value = .{ .int = @intCast(ent.span.byteEnd) } },
                .{ .key = "line", .value = .{ .int = @intCast(ent.span.line) } },
                .{ .key = "col", .value = .{ .int = @intCast(ent.span.col) } },
            });
            try ef.append(alloc, .{ .key = "span", .value = .{ .object = span_fields } });
            try ef.append(alloc, .{ .key = "emitter", .value = .{ .string = ent.emitter } });
            try ef.append(alloc, .{ .key = "inputsHash", .value = .{ .string = try inputsHash(alloc, ent.inputs_projection) } });
            out_entries[i] = .{ .object = try ef.toOwnedSlice(alloc) };
        }
        try art_fields.append(alloc, .{ .key = path, .value = .{ .array = out_entries } });
    }

    const doc_fields = try alloc.dupe(Value.Field, &.{
        .{ .key = "version", .value = .{ .int = 1 } },
        .{ .key = "source", .value = .{ .string = source_file } },
        .{ .key = "artifacts", .value = .{ .object = try art_fields.toOwnedSlice(alloc) } },
    });
    return .{ .object = doc_fields };
}

fn lessStr(_: void, a: []const u8, b: []const u8) bool {
    return std.mem.lessThan(u8, a, b);
}

const SPAN_KEY = "span";

/// Error set for the JSON serializers — `writeValueCompact` bottoms out in
/// `bufPrint` (numbers), which can return `error.NoSpaceLeft` in addition to the
/// allocator's `OutOfMemory`. Mutually-recursive fns need an explicit set.
const WriteError = error{ OutOfMemory, NoSpaceLeft };

/// A value rendered on one line with `", "` / `": "` spacing (the span object).
fn jsonInline(buf: *Buf, alloc: Alloc, v: Value) WriteError!void {
    switch (v) {
        .object => |fields| {
            if (fields.len == 0) {
                try buf.appendSlice(alloc, "{}");
                return;
            }
            try buf.appendSlice(alloc, "{ ");
            for (fields, 0..) |f, i| {
                if (i != 0) try buf.appendSlice(alloc, ", ");
                try json_canon.writeJsonString(buf, alloc, f.key);
                try buf.appendSlice(alloc, ": ");
                try jsonInline(buf, alloc, f.value);
            }
            try buf.appendSlice(alloc, " }");
        },
        .array => |items| {
            if (items.len == 0) {
                try buf.appendSlice(alloc, "[]");
                return;
            }
            try buf.append(alloc, '[');
            for (items, 0..) |x, i| {
                if (i != 0) try buf.appendSlice(alloc, ", ");
                try jsonInline(buf, alloc, x);
            }
            try buf.append(alloc, ']');
        },
        else => try jsonScalar(buf, alloc, v),
    }
}

/// A scalar JSON value (json.dumps, ensure_ascii=False).
fn jsonScalar(buf: *Buf, alloc: Alloc, v: Value) WriteError!void {
    try json_canon.writeValueCompact(buf, alloc, v);
}

/// Pretty-print `v` at a 2-space indent, but render any `span` object inline.
/// Object keys keep insertion order; list items keep order. Mirrors `_json_pretty`.
fn jsonPretty(buf: *Buf, alloc: Alloc, v: Value, level: usize) WriteError!void {
    switch (v) {
        .object => |fields| {
            if (fields.len == 0) {
                try buf.appendSlice(alloc, "{}");
                return;
            }
            try buf.appendSlice(alloc, "{\n");
            for (fields, 0..) |f, i| {
                try writeIndent(alloc, buf, level + 1);
                try json_canon.writeJsonString(buf, alloc, f.key);
                try buf.appendSlice(alloc, ": ");
                if (std.mem.eql(u8, f.key, SPAN_KEY)) {
                    try jsonInline(buf, alloc, f.value);
                } else {
                    try jsonPretty(buf, alloc, f.value, level + 1);
                }
                if (i != fields.len - 1) try buf.append(alloc, ',');
                try buf.append(alloc, '\n');
            }
            try writeIndent(alloc, buf, level);
            try buf.append(alloc, '}');
        },
        .array => |items| {
            if (items.len == 0) {
                try buf.appendSlice(alloc, "[]");
                return;
            }
            try buf.appendSlice(alloc, "[\n");
            for (items, 0..) |x, i| {
                try writeIndent(alloc, buf, level + 1);
                try jsonPretty(buf, alloc, x, level + 1);
                if (i != items.len - 1) try buf.append(alloc, ',');
                try buf.append(alloc, '\n');
            }
            try writeIndent(alloc, buf, level);
            try buf.append(alloc, ']');
        },
        else => try jsonScalar(buf, alloc, v),
    }
}

/// Serialize a provenance document to the §6.2 fixture bytes: 2-space indent,
/// inline `span` objects, trailing newline.
pub fn provenanceJsonText(alloc: Alloc, provenance_doc: Value) ![]u8 {
    var buf: Buf = .empty;
    try jsonPretty(&buf, alloc, provenance_doc, 0);
    try buf.append(alloc, '\n');
    return buf.toOwnedSlice(alloc);
}

// --------------------------------------------------------------------------- //
// The lowering driver.
// --------------------------------------------------------------------------- //

pub const LowerError = error{OutOfMemory};

/// Lower a *validated* graph to artifacts + a provenance manifest (0012). When
/// `items` is supplied, `enrichGraph` runs first (recovers the fiber policy
/// sub-block). Pure: no IO (the caller writes `result.files`).
pub fn lower(alloc: Alloc, graph: *Graph, items: ?[]const ast.Item) LowerError!LowerResult {
    if (items) |its| try enrichGraph(alloc, graph, its);

    const rv_opt = runtimeView(alloc, graph) catch return error.OutOfMemory;

    if (rv_opt == null) {
        const prov = (buildProvenance(alloc, graph.source_file, &.{})) catch return error.OutOfMemory;
        return LowerResult{ .files = .empty, .provenance = prov };
    }
    const rv = rv_opt.?;

    var files: Files = .empty;
    var all_entries: std.ArrayList(ProvEntry) = .empty;

    const Run = struct {
        fn merge(a: Alloc, dst: *Files, ents: *std.ArrayList(ProvEntry), out: EmitOut) !void {
            var it = out.files.iterator();
            while (it.next()) |e| try dst.put(a, e.key_ptr.*, e.value_ptr.*);
            for (out.entries) |x| try ents.append(a, x);
        }
    };

    // ALWAYS: the Nix spine + docs.
    Run.merge(alloc, &files, &all_entries, emitNixSpine(alloc, graph, rv) catch return error.OutOfMemory) catch return error.OutOfMemory;
    Run.merge(alloc, &files, &all_entries, emitDocsRuntime(alloc, graph, rv) catch return error.OutOfMemory) catch return error.OutOfMemory;

    // Direct: per-fiber Zig daemon configs.
    if (rv.fibers.len > 0) {
        Run.merge(alloc, &files, &all_entries, emitZigDaemonCfg(alloc, graph, rv, rv.fibers) catch return error.OutOfMemory) catch return error.OutOfMemory;
    }

    // Direct: catalog.jsonl for indexes that select it.
    var jsonl_indexes: std.ArrayList(*GraphNode) = .empty;
    for (rv.indexes) |i| {
        if (hasTarget(indexEmitTargets(alloc, i) catch return error.OutOfMemory, "catalog.jsonl")) {
            jsonl_indexes.append(alloc, i) catch return error.OutOfMemory;
        }
    }
    if (jsonl_indexes.items.len > 0) {
        Run.merge(alloc, &files, &all_entries, emitCatalogJsonl(alloc, graph, jsonl_indexes.items) catch return error.OutOfMemory) catch return error.OutOfMemory;
    }

    const provenance = buildProvenance(alloc, graph.source_file, all_entries.items) catch return error.OutOfMemory;
    return LowerResult{ .files = files, .provenance = provenance };
}

